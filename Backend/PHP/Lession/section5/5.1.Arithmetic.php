<?php
// Phép toán số học trong PHP
$a = 10;
$b = 5;

// Phép toán cộng $a + $b
echo "$a + $b = " . ($a + $b);
echo "<br>";

// Phép toán trừ
echo "$a - $b = " . ($a - $b);
echo "<br>";

// Phép toán nhân
echo "$a * $b = " . ($a * $b);
echo "<br>";

// Phép toán chia
echo "$a / $b = " . ($a / $b);
echo "<br>";

// Phép toán chia lấy dư
echo "$a % $b = " . ($a % $b);
echo "<br>";

// Phép toán mũ
echo "$a ^ $b = " . (pow($a, $b));
echo "<br>";
